#include "libft.h"

t_list	*ft_lstnew(void *content)
{
	t_list	*elem;

	elem = malloc(sizeof(t_list));
	if (elem == NULL)
		return (0);
	elem->content = content;
	elem->next = 0;
	return (elem);
}
